// Задаємо числа для додавання
const a = 1234;
const b = 5678;

// Запам'ятовуємо час на початку виконання задачі
const startTime = new Date();

// Запитуємо відповідь від користувача
const answer = prompt(`Скільки буде ${a} + ${b}?`);

// Запам'ятовуємо час після введення відповіді
const endTime = new Date();

// Обчислюємо різницю між часами в секундах
const timeDiff = (endTime - startTime) / 1000;

// Перевіряємо відповідь і виводимо результат
if (answer == a + b) {
	console.log(`Правильно! Задачу вирішено за ${timeDiff} секунд.`);
} else {
	console.log(`Неправильно. Задачу вирішено за ${timeDiff} секунд.`);
}







